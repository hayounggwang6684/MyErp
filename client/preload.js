const { contextBridge, ipcRenderer } = require("electron");

contextBridge.exposeInMainWorld("erpClient", {
  getAppVersion: () => ipcRenderer.invoke("app:version"),
  getAccessScope: () => ipcRenderer.invoke("auth:access-scope"),
  login: (payload) => ipcRenderer.invoke("auth:login", payload),
  changePassword: (payload) => ipcRenderer.invoke("auth:change-password", payload),
  verifyMfa: (payload) => ipcRenderer.invoke("auth:verify-mfa", payload),
  startMfaEnrollment: () => ipcRenderer.invoke("auth:mfa-enrollment:start"),
  getMfaEnrollmentStatus: () => ipcRenderer.invoke("auth:mfa-enrollment:status"),
  verifyMfaEnrollment: (payload) => ipcRenderer.invoke("auth:mfa-enrollment:verify", payload),
  logout: () => ipcRenderer.invoke("auth:logout"),
  getSession: () => ipcRenderer.invoke("session:get"),
  listCustomers: (search) => ipcRenderer.invoke("customers:list", search),
  getCustomer: (customerId) => ipcRenderer.invoke("customers:get", customerId),
  createCustomer: (payload) => ipcRenderer.invoke("customers:create", payload),
  updateCustomer: (customerId, payload) => ipcRenderer.invoke("customers:update", customerId, payload),
  updateCustomerMemo: (customerId, payload) => ipcRenderer.invoke("customers:update-memo", customerId, payload),
  addCustomerContact: (customerId, payload) => ipcRenderer.invoke("customers:add-contact", customerId, payload),
  updateCustomerContact: (contactId, payload) => ipcRenderer.invoke("customers:update-contact", contactId, payload),
  addCustomerAddress: (customerId, payload) => ipcRenderer.invoke("customers:add-address", customerId, payload),
  addCustomerAsset: (customerId, payload) => ipcRenderer.invoke("customers:add-asset", customerId, payload),
  updateCustomerAsset: (assetId, payload) => ipcRenderer.invoke("customers:update-asset", assetId, payload),
  deleteCustomerAsset: (assetId) => ipcRenderer.invoke("customers:delete-asset", assetId),
  addAssetEquipment: (assetId, payload) => ipcRenderer.invoke("customers:add-equipment", assetId, payload),
  updateAssetEquipment: (equipmentId, payload) => ipcRenderer.invoke("customers:update-equipment", equipmentId, payload),
  deleteAssetEquipment: (equipmentId) => ipcRenderer.invoke("customers:delete-equipment", equipmentId),
  createMasterDataRequest: (payload) => ipcRenderer.invoke("master-data-requests:create", payload),
  listEquipmentOptions: (optionType) => ipcRenderer.invoke("customers:list-equipment-options", optionType),
  listEngineModels: (search) => ipcRenderer.invoke("customers:list-engine-models", search),
  createEngineModel: (payload) => ipcRenderer.invoke("customers:create-engine-model", payload),
  listGearboxModels: (search) => ipcRenderer.invoke("customers:list-gearbox-models", search),
  createGearboxModel: (payload) => ipcRenderer.invoke("customers:create-gearbox-model", payload),
  selectBusinessLicenseFile: () => ipcRenderer.invoke("customers:select-business-license-file"),
  uploadFile: (payload) => ipcRenderer.invoke("customers:upload-file", payload),
  linkFile: (fileId, payload) => ipcRenderer.invoke("customers:link-file", fileId, payload),
  extractBusinessLicense: (customerId, payload) => ipcRenderer.invoke("customers:extract-business-license", customerId, payload),
  listOrders: () => ipcRenderer.invoke("orders:list"),
  saveOrder: (orderId, payload) => ipcRenderer.invoke("orders:save", orderId, payload),
  deleteOrder: (orderId) => ipcRenderer.invoke("orders:delete", orderId),
  mergeOrders: (payload) => ipcRenderer.invoke("orders:merge", payload),
  listProjects: () => ipcRenderer.invoke("projects:list"),
  getAssetWorkspace: () => ipcRenderer.invoke("assets:workspace"),
  savePhysicalAssetRecord: (assetId, payload) => ipcRenderer.invoke("assets:physical:save", assetId, payload),
  deletePhysicalAssetRecord: (assetId) => ipcRenderer.invoke("assets:physical:delete", assetId),
  saveKnowledgeRecord: (recordId, payload) => ipcRenderer.invoke("assets:knowledge:save", recordId, payload),
  deleteKnowledgeRecord: (recordId) => ipcRenderer.invoke("assets:knowledge:delete", recordId),
  selectDocumentFile: () => ipcRenderer.invoke("documents:select-file"),
  getPreference: () => ipcRenderer.invoke("preference:get"),
  savePreference: (payload) => ipcRenderer.invoke("preference:save", payload),
  checkForUpdates: () => ipcRenderer.invoke("updates:check"),
  openUpdateDownload: (url) => ipcRenderer.invoke("updates:open-download", url),
  openDocument: (target) => ipcRenderer.invoke("documents:open", target),
  printHtml: (payload) => ipcRenderer.invoke("print:html", payload),
  onUpdateStatus: (handler) => {
    ipcRenderer.removeAllListeners("update-status");
    ipcRenderer.on("update-status", (_event, payload) => handler(payload));
  },
});
